package com.example.shubhangi;

public class Boolean {
    public static void main(String[] args) {
        boolean A=true;
        boolean B=false;
        System.out.println("A|B="+(A|B));
        System.out.println("A&B="+(A&B));
        System.out.println("|A="+(A^B));
        System.out.println("A^B="+(A^B));

    }
}

